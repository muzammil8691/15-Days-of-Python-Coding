#Create a list of fruits and access elements using indexing.

fruits = ['Banana', 'Apple', 'Mango', 'Grapes', 'Watermelon', 'Kiwi']

print(fruits[2]) #index 2
print(fruits[-4]) #index -4
print(fruits[-4: -2]) ##from index -4 to -2 (Not including -2)
print(fruits[:3]) #from index 0 to 3 (Not including 3)
print(fruits[1:5:2]) # from index 1 to 5 (Not including 3) with a step of 2
print(fruits[::3])  # from index 0 to end with a step of 3